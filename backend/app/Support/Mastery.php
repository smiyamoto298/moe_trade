    /** 有効なマスタリコードの一覧 */
    public static function codes(): array
    {
        return array_keys(self::ALL);
    }

    /** 指定スキルを構成に含むマスタリのコード一覧 */
    public static function codesForSkill(string $skill): array
    {
        $codes = [];
        foreach (self::ALL as $code => $m) {
            if (in_array($skill, $m['skills'], true)) {
                $codes[] = $code;
            }
        }
        return $codes;
    }

    /**
     * 構成スキルが「すべて」選択スキルに含まれるマスタリのコード一覧（完全一致用）。
     * 例: 刀剣・キック・盾・戦闘技術がすべて選択されているときに WAR を返す。
     */
    public static function fullyCoveredCodes(array $selectedSkills): array
    {
        $codes = [];
        foreach (self::ALL as $code => $m) {
            // 構成スキルが選択スキルの部分集合（不足スキルが無い）なら完全一致
            if (empty(array_diff($m['skills'], $selectedSkills))) {
                $codes[] = $code;
            }
        }
        return $codes;
    }
